<script>
import { mapState, mapActions } from "vuex";
import Helper from "./../../../Helper/moment";

export default {
  mixins: [Helper],
  name: "ModuleList",
  data() {
    return {
      moduleData: [],
    };
  },
  computed: {
    ...mapState("module", ["module"]),
  },
  mounted() {
    this.moduleData = [];
    this.getModules(this.$router.currentRoute.query).then((data)=>{
      this.moduleData = data;
    })
  },
  methods: {
    ...mapActions("module", ["getModules","moduleDelete"]),

    moduleMethod(){
      this.moduleData = [];
      this.getModules(this.$router.currentRoute.query)
          .then((data)=>{
            this.moduleData = data;
          })
    },

    deleteModule(id){
      console.log(id);
      this.$swal({
              title: 'Are you sure?',
              text: "You won't be able to revert this!",
              type: 'warning',
              showCancelButton: true,
              confirmButtonColor: '#3085d6',
              cancelButtonColor: '#d33',
              confirmButtonText: 'Yes, delete it!'
            }).then((result) => {
              if (result.value) {
                this.moduleDelete(id).then(()=>{
                    this.$swal(
                      'Deleted!',
                      'Module has been deleted.',
                      'success'
                    )
                  this.moduleMethod();  
                })
                
              }
            });
    }
  },
};
</script>

